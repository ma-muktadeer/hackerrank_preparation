package test.mytry.MakeListFromList;
public class Fruits {
    private int a;
    private String name;
    private double value;
    public Fruits(int a, String name, double value) {
        this.a = a;
        this.name = name;
        this.value = value;
    }
    public int getA() {
        return a;
    }
    public void setA(int a) {
        this.a = a;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }
}
